package ex_01_Java_Basics_Part1;

import javax.swing.*;

public class    Lab0011_CommonMath {
    public static void main(String[] args) {
        System.out.println("2+2"); // Prints concatenated string i.e. 2+2, what ever may be operator, if it is in double quotes, it will be considered as String
        System.out.println(2+2); //Prints addition result i.e. 4
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println(2-2);
        System.out.println(3/2); // Always prints integer, not decimal as the given no's are integers
    }
}
